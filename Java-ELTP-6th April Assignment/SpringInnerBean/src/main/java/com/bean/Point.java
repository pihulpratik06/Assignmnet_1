package com.bean;
//We have two Points
//Two Co-ordinates x and y
public class Point {

     private int x;
     private int y;

     public int getX() {
          return x;
     }

     public int getY() {
          return y;
     }

     public Point(int x, int y) {
          this.x = x;
          this.y = y;
     }
}
